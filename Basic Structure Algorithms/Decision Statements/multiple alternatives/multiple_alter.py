#Algo 0.4.3: Using Multiple Alternatives

#Step 01: Reading input
VAR = int(input("Enter number: "))
#Step 02: Using Multiple Alternatives
#[a. Checking if greater than 0]
if VAR > 0:
    print(VAR, " is greater than 0")
#[b. Checking if less than 0]
elif VAR < 0:
    print(VAR, " is less than 0")
#[c. Declaring as 0]
else:
    print(VAR, " is equal to 0")

    
